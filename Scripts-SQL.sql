      ON UPDATE NO ACTION,
  FOREIGN KEY(ORGANIZACAO_IDORGANIZACAO)
    REFERENCES ORGANIZACAO(IDORGANIZACAO)
      ON DELETE NO ACTION
      ON UPDATE NO ACTION);



